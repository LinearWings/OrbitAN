export function getT(lang: Lang): Record<string, string> {
  return (translations as Record<string, Record<string, string>>)[lang] || translations.zh;
}

export function detectLang(acceptLanguage?: string | null): Lang {
  if (!acceptLanguage) return "zh";
  if (acceptLanguage.startsWith("zh")) return "zh";
  if (acceptLanguage.startsWith("en")) return "en";
  return "zh";
}
