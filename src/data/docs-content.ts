const overview = `# 概述 · Orbit Mode 聚焦视野

Orbit Mode 是轨道计划的核心聚焦工作空间。它把日程表中的普通任务升级为带方法论的聚焦区块，让你在同一个界面里完成计划、执行、复盘和策略判断。

## 聚焦区块

每个聚焦区块包含四个要素：

- 明确的任务目标，可关联到某个日程任务
- 一个时间范围，例如 09:00 到 10:30
- 一种时间管理方法论
- 当前执行状态：计划中、进行中、暂停或已完成

## 六大方法论

| 方法论 | 核心思想 | 适用场景 |
|--------|----------|----------|
| GTD 搞定 | 清空大脑，把任务放进可信系统 | 事务繁杂、容易遗漏 |
| 番茄工作法 | 专注、休息、记录、复盘的闭环 | 容易分心、需要节奏约束 |
| 帕累托法则 | 找到少数高杠杆关键任务 | 任务过多、需要优先级判断 |
| 莫法特休息法 | 通过切换任务类型恢复精力 | 长时间学习或多类工作并行 |
| 豪威尔矩阵 | 区分重要和紧急，减少救火 | 优先级混乱、长期目标被挤压 |
| SWOT 分析 | 从内外部因素做策略选择 | 项目启动、周复盘、自我洞察 |

## 使用方式

1. 在日程界面进入 Orbit Mode。
2. 新建聚焦区块，选择时间范围和方法论。
3. 点击聚焦区块打开对应方法面板。
4. 按方法论提示执行，并把过程数据保存在本地。

> 方法论不是装饰标签。它决定你在这段时间里如何思考、如何取舍，以及如何复盘。`;

